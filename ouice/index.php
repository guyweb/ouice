    <li>How can I make my website look good on mobile phones?</li>
    <li>How can I make a print version of my web pages?</li>
</ol>

<p>OU ICE answers all these questions and more.</p>

<h3>Consistent user experience</h3>

<p>Plenty of people build websites for the OU. Staff across various departments and external providers all contribute to the OU web site, but until now there has been no effective way to help everybody use the same user interface elements. There have been plenty of attempts at documenting standards and procedures yet still the OU website is difficult to navigate. There are so many different solutions to the same problem that our students have to re-learn different interfaces as they move from OU home to StudentHome to the Careers Advisory Service to the Library to the VLE. </p>

<p>In the past there were standards for fonts, colours and headers and footers. Now there are design patterns and components for all sorts of commonly used web elements like video players, search forms, FAQ lists, tips and info boxes. There are plenty of layouts and colour schemes to satisfy the needs of the most demanding client. OU ICE is about designing for a consistent user experience whilst encouraging innovation and diversity. </p>

<h3>Collaborative problem solving</h3>

<p>OU ICE is a regularly released collaborative effort, facilitated by Online Services. Everybody can submit problems and solutions to everyday web design issues. OU ICE will grow with the demands of the evolving web. OU ICE will help us be more productive, reduce duplication of effort and make it much easier to commission work form external suppliers.</p>

<p>Using OU ICE is easy. Simply download the core HTML template, configure it to your requirements and add the code snippets you need. OU ICE is fully documented and benefits from a regular release cycle. When new features are released they are immediately available to all OU ICE sites. </p>

<p>OU ICE is the default OU Drupal theme but it is not exclusive to Drupal. In fact, OU ICE is CMS agnostic. It works just as well on Wordpress, MediaWiki, Dreamweaver and many other types of website. The only requirement is a web server running either PHP or ASP.net.</p>

<p>OU ICE lets developers focus on making great site-specific features without having to worry about thinking about how it will look in print or on a mobile phone.</p>

