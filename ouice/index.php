<p>Consider these questions:</p>

<ol>
    <li>How do I create an OU website theme?</li>
    <li>How do I make sure my website is on-brand?</li>
    <li>How can I make sure my website meets the OU accessibility requirements?</li>
    <li>What should appear in the OU header and footer?</li>
    <li>What&rsquo;s the official OU logo for use on OU websites?</li>
    <li>What&rsquo;s the process for adding the OU header and footer to my site?</li>
