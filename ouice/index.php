<p class="ou-introduction">The Open University has a set of online<strong> design patterns</strong>, <strong>components</strong> and <strong>templates</strong> so we can provide our users with a <strong>consistent user experience</strong>. We call this <strong>OU ICE</strong>. </p>

<p>OU ICE is a front-end framework of HTML, CSS and Javascript to enable rapid development of accessible, brand compliant Open University websites. This documentation covers the current version: <strong>OU ICE v4</strong>.</p>

<h2>OU ICE</h2>

<ul>
  <li>enables rapid development of usable, accessible and brand compliant web sites</li>
  <li>provides foundation visual and behavioral layers upon which distinctive and unique websites can be grown</li>
  <li>makes a web designer's job easier.</li>
</ul>

<h2>OU ICE is</h2>

<ul>
  <li>the &ldquo;Look and feel&rdquo; of Open University websites for full screen, mobile and print versions</li>
  <li>the layout, fonts, colours and graphics of Open University websites</li>
  <li>a&ldquo;front-end&rdquo;, &ldquo;client-side&rdquo; framework</li>
  <li>a CSS framework for Open University websites</li>
  <li>a set of HTML markup patterns</li>
  <li>visual design components and patterns</li>
  <li>a  visual language for Open University web sites</li>
  <li>web content management agnostic.</li>
</ul>

<h2>OU ICE is not</h2>

<ul>
  <li>a Content Management System (CMS) -- That would be Drupal, Wordpress, MediaWiki, Assisted Publishing System, Adobe Contribute</li>
  <li>a PHP development framework</li>
  <li>an ASP development framework</li>
  <li>a database system</li>
  <li>a programming language</li>
  <li>the Online Services team.</li>
</ul>

